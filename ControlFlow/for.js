//The three parts are as below:
//1: Initital Expression
//2: condition or lenth usually
//3: increment in iteration

for (let i = 0; i < 5; i++) {
  console.log("Hello World", i);
}

for (let i = 5; i >= 1; i--) {
  if (i % 2 === 0) console.log(i);
}

//Hello World is printed 5 times as the loop is conditioned.
