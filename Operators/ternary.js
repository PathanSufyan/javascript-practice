//Ternary Operator-----------------------------

// //If a customer has more than 100 points,
// They are a 'gold' customer, otherwise,
//  they are a 'silver' customer.

//Ternary Operator: (your condition here) ? ( then do this) : (else do this )

let points = 10;
let type = points >= 100 ? "gold" : "silver"; //(Shorthand for if else)
console.log(type);
